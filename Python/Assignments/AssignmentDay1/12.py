# Integer to Float
x = 10
y = float(x)  
print("Integer to Float:", y)

# Float to Integer
x = 10.5
y = int(x)  
print("Float to Integer:", y)

# Integer to String
x = 10
y = str(x)  
print("Integer to String:", y)

# String to Integer
x = "10"
y = int(x)  
print("String to Integer:", y)

# String to Float
x = "10.5"
y = float(x)  
print("String to Float:", y)

# Float to String
x = 10.5
y = str(x)  
print("Float to String:", y)

# Integer to Complex
x = 10
y = complex(x) 
print("Integer to Complex:", y)

# Float to Complex
x = 10.5
y = complex(x)  
print("Float to Complex:", y)

# String to Complex
x = "10+5j"
y = complex(x)  
print("String to Complex:", y)

# List to Tuple
x = [1, 2, 3]
y = tuple(x)  
print("List to Tuple:", y)

# Tuple to List
x = (1, 2, 3)
y = list(x)  
print("Tuple to List:", y)

# List to Set
x = [1, 2, 2, 3]
y = set(x)  
print("List to Set:", y)
